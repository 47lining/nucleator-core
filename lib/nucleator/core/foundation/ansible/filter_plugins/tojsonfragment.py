import json

def to_json_fragment(Entries, stripdepth=1):
        """Return a json fragment representing the provided objects"""

        return json.dumps(Entries, indent=4)[stripdepth:][:-stripdepth]

class FilterModule(object):
    ''' Custom filters are loaded by FilterModule objects '''

    def filters(self):
        ''' FilterModule objects return a dict mapping filter names to
            filter functions. '''
        return {
            'to_json_fragment': to_json_fragment
        }
